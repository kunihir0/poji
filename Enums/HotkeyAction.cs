namespace poji.Enums
{
    /// <summary>
    /// Defines the actions that can be triggered by hotkeys.
    /// </summary>
    public enum HotkeyAction
    {
        /// <summary>
        /// Exit the application.
        /// </summary>
        Exit,

        /// <summary>
        /// Toggle the visibility of the application.
        /// </summary>
        ToggleVisibility,

        /// <summary>
        /// Reload the crosshair.
        /// </summary>
        ReloadCrosshair,

        /// <summary>
        /// Switch to a different monitor.
        /// </summary>
        SwitchMonitor,

        /// <summary>
        /// Toggle debug mode.
        /// </summary>
        ToggleDebugMode,

        /// <summary>
        /// Toggle recoil simulation.
        /// </summary>
        ToggleRecoilSimulation,

        /// <summary>
        /// Cycle through different render modes.
        /// </summary>
        CycleRenderMode
    }
}
